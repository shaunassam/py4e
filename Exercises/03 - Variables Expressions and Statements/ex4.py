## Show value and type for each value

width = 17
height = 12.0

print("Width:", width, type(width))
print("Height:", height, type(height))

print("#1) width//2")
no1 = width//2
print(no1)
print(type(no1))

print("#2) width/2.0")
no2 = width/2.0
print(no2)
print(type(no2))

print("#3) height/3")
no3 = height/3
print(no3)
print(type(no3))

print("#4) 1 + 2 * 5")
no4 = 1 + 2 * 5
print(no4)
print(type(no4))